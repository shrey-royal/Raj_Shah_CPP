#include<iostream>
using namespace std;

int main() {
    int a;

    //to print the data on the screen
    // cout << "Hello World!" << endl;

    cout << "Enter a number: ";
    cin >> a;

    cout << "The number you entered is: " << a << endl;

    return 0;
}

/*
cout : console output : used to print the data on the screen
<< : insertion operator

cin : console input : used to take input from the user
>> : extraction operator

endl : end line : when we use endl, the cursor will move to the next line : \n

*/